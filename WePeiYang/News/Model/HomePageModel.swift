//    let visitcount: String
}

struct Service: Codable {
    let lost: [JSONAny]
    let found: [Found]
}

struct Found: Codable {
    let id, name, title, place: String
    let time, phone, detailType, isback: String
    let picture: String

    enum CodingKeys: String, CodingKey {
        case id, name, title, place, time, phone
        case detailType = "detail_type"
        case isback, picture
    }
}

